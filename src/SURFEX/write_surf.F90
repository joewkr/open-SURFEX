#ifdef SFX_OL
USE MODE_WRITE_SURF_OL, ONLY: WRITE_SURF0_OL, WRITE_SURF0_TIME_OL
#endif
#ifdef SFX_LFI
USE MODE_WRITE_SURF_LFI, ONLY: WRITE_SURF0_LFI
#endif
#ifdef SFX_NC
USE MODE_WRITE_SURF_NC, ONLY: WRITE_SURF0_NC
#endif
