SUBROUTINE SET_SURFEX_FILE_NAME_FA(HNAME_IN,HNAME_OUT)
!
USE MODD_IO_SURF_FA,  ONLY : CFILEIN_FA
!
USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK
USE PARKIND1  ,ONLY : JPRB
!
IMPLICIT NONE
!
 CHARACTER(LEN=*), OPTIONAL, INTENT(IN)     :: HNAME_IN
 CHARACTER(LEN=*), OPTIONAL, INTENT(OUT)    :: HNAME_OUT
REAL(KIND=JPRB) :: ZHOOK_HANDLE
!
IF (LHOOK) CALL DR_HOOK('SET_SURFEX_FILE_NAME_FA',0,ZHOOK_HANDLE)
!
IF (PRESENT(HNAME_IN)) THEN
  CFILEIN_FA=HNAME_IN
ELSEIF (PRESENT(HNAME_OUT)) THEN
  HNAME_OUT=CFILEIN_FA
ENDIF
!
IF (LHOOK) CALL DR_HOOK('SET_SURFEX_FILE_NAME_FA',1,ZHOOK_HANDLE)
!
END SUBROUTINE SET_SURFEX_FILE_NAME_FA
