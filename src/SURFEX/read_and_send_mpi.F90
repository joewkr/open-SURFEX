SUBROUTINE READ_AND_SEND_MPI_N3D(KWORK,KWORK2,KMASK,KPIO,KDX)
!
USE MODD_SURFEX_MPI, ONLY : NINDEX, NPROC, NRANK, NCOMM, NPIO, NSIZE, &
                            XTIME_NPIO_READ, XTIME_COMM_READ, IDX_R, WLOG_MPI, &
                            NREQ
