    ELSE
      IWORK3(:,:,:) = IWORK3S(:,:,:,I+1)
    ENDIF
    !
IF (LHOOK) CALL DR_HOOK('READ_AND_SEND_MPI_N3D_3',1,ZHOOK_HANDLE_OMP)
  ENDDO
!$OMP END PARALLEL DO
  !  
ELSE
  !  
  IDX = IDX + 1
  !
IF (LHOOK) CALL DR_HOOK('READ_AND_SEND_MPI_N3D_1',1,ZHOOK_HANDLE)
IF (LHOOK) CALL DR_HOOK('READ_AND_SEND_MPI_N3D_2',0,ZHOOK_HANDLE)  
#ifdef SFX_MPI 
  !
  XTIME0 = MPI_WTIME()  
