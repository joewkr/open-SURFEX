!$OMP PARALLEL DO PRIVATE(I,ICPT,J,INFOMPI,IREQ,ZHOOK_HANDLE_OMP)  
  DO I=0,NPROC-1
IF (LHOOK) CALL DR_HOOK('READ_AND_SEND_MPI_N3D_2',0,ZHOOK_HANDLE_OMP)
    !
#ifdef SFX_MPI    
    XTIME0 = MPI_WTIME()
#endif    
    ! 
    ICPT = 0
    !
    IWORK3S(:,:,:,I+1) = 0.
    !
