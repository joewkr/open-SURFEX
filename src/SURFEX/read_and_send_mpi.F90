        IWORK3S(ICPT,:,:,I+1) = KWORK(J,:,:)
      ENDIF
      !
    ENDDO
IF (LHOOK) CALL DR_HOOK('READ_AND_SEND_MPI_N3D_2',1,ZHOOK_HANDLE_OMP)
IF (LHOOK) CALL DR_HOOK('READ_AND_SEND_MPI_N3D_3',0,ZHOOK_HANDLE_OMP)
    !
