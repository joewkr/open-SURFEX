  IF (ANY(DTI%LDATA_DMAX)) THEN
    ALLOCATE(DTI%XPAR_DMAX      (KDIM,NVEGTYPE))
    IF (GDIM) THEN
      YRECFM='D_DMAX_'
    ELSE
      YRECFM='D_DMAX'
    ENDIF
    CALL READ_SURF_ISBA_PAR_n(DTCO, U, GCP, IO%NPATCH, HPROGRAM, YRECFM, ILUOUT, KSIZE, &
                                IVERSION, IBUGFIX, DTI%LDATA_DMAX,DTI%XPAR_DMAX(:,:),IRESP,&
