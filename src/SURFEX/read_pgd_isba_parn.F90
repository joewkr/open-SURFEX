    END DO
  ENDIF
!
  IF (ANY(DTI%LDATA_GROUND_DEPTH)) THEN
    ALLOCATE(DTI%XPAR_GROUND_DEPTH(KDIM,NVEGTYPE))
    IF (GDIM) THEN
      YRECFM2='D_GRDPT_'
    ELSE
      YRECFM2='D_GROUND_DEPTH'
       IF (IVERSION>7 .OR. IVERSION==7 .AND. IBUGFIX>=3) YRECFM2='D_GROUND_DPT'
