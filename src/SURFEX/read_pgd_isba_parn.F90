      DO JL=1,SIZE(DTI%XPAR_WWILT,2)
        WRITE(YRECFM,FMT='(A9,I2.2)') 'D_WWILT_L',JL
        CALL READ_SURF(HPROGRAM,YRECFM,ZWORK(:,JL),IRESP,HCOMMENT=YCOMMENT,HDIR=YDIR)
      ENDDO
      IF (KDIM/=KSIZE) THEN
